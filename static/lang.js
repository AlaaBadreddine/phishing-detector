function translatePage(lang) {
    const translation = translations[lang] || translations.en;
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.dataset.i18n;
        if (translation[key] !== undefined) {
            element.textContent = translation[key];
        }
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.dataset.i18nPlaceholder;
        if (translation[key] !== undefined) {
            element.placeholder = translation[key];
        }
    });
    document.documentElement.lang = lang;
    document.documentElement.dir = translation.dir || 'ltr';
}

function setLanguage(lang) {
    const chosen = translations[lang] ? lang : 'en';
    translatePage(chosen);
    localStorage.setItem('phishLang', chosen);
    const select = document.getElementById('languageSelect');
    if (select) {
        select.value = chosen;
    }
    const button = document.getElementById('langButton');
    if (button) {
        button.textContent = chosen.toUpperCase() + ' ▾';
    }
}

function initLanguage() {
    const stored = localStorage.getItem('phishLang') || 'en';
    setLanguage(stored);
    const select = document.getElementById('languageSelect');
    if (select) {
        select.addEventListener('change', event => setLanguage(event.target.value));
    }
    const langButton = document.getElementById('langButton');
    const langMenu = document.getElementById('langMenu');
    if (langButton && langMenu) {
        langButton.addEventListener('click', () => langMenu.classList.toggle('hidden'));
        langMenu.querySelectorAll('.lang-option').forEach(button => {
            button.addEventListener('click', () => {
                setLanguage(button.dataset.lang);
                langMenu.classList.add('hidden');
            });
        });
        document.addEventListener('click', event => {
            if (!langButton.contains(event.target) && !langMenu.contains(event.target)) {
                langMenu.classList.add('hidden');
            }
        });
    }
}

window.addEventListener('DOMContentLoaded', initLanguage);
